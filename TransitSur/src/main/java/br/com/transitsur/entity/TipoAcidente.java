package br.com.transitsur.entity;

public class TipoAcidente {
	
	int IdTipoAcidente;
	String NomeAcidente;
	
	public int getIdTipoAcidente() {
		return IdTipoAcidente;
	}
	public void setIdTipoAcidente(int idTipoAcidente) {
		IdTipoAcidente = idTipoAcidente;
	}
	public String getNomeAcidente() {
		return NomeAcidente;
	}
	public void setNomeAcidente(String nomeAcidente) {
		NomeAcidente = nomeAcidente;
	}

}
